    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int escolha = JOptionPane.showConfirmDialog(null, "A interação no seu computador pode ser inesperada.\n\nMesmo assim... Deseja continuar?");
        if (escolha == JOptionPane.YES_OPTION) {
            this.dispose();
            try {
                this.finalize();
                new WNDMainWindow().setVisible(true);
                System.gc();
            } catch (Throwable ex) {
                Logger.getLogger(INTKoulikov.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            int escolha2 = JOptionPane.showConfirmDialog(null, "Você já tentou 'Reparar o Problema Automaticamente'?");
            if (escolha2 == JOptionPane.NO_OPTION) {
                int escolha3 = JOptionPane.showConfirmDialog(null, "Quer tentar?");
                if (escolha3 == JOptionPane.YES_OPTION) {
                    jButton1.doClick();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Se não funcionou, seu computador pode ser incompatível com a resolução do jogo.");

            }
        }
        this.toFront();
    }//GEN-LAST:event_jButton2ActionPerformed

