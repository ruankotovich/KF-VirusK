    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        GraphicsDevice gd = (GraphicsEnvironment.getLocalGraphicsEnvironment()).getDefaultScreenDevice();
        if (gd.isDisplayChangeSupported()) {
            gd.setDisplayMode(new DisplayMode(1366, 768, 32, 60));
        } else {
            int esc = JOptionPane.showConfirmDialog(null, "A resolução não pode ser alterada devido às permissões do jogo!\n\nDeseja tentar um programa externo? (Somente Windows)");
            if (esc == JOptionPane.YES_OPTION) {
                if ((System.getProperty("os.name").toLowerCase()).contains("windows")) {
                    try {
                        Runtime.getRuntime().exec(this.getClass().getResource("/system/resource/mr.exe").getFile() + " /1:1366,768,32,60");
                        JOptionPane.showMessageDialog(null, "Agora possivelmente seu problema foi resolvido!");
                        this.dispose();
                        new CTRKoulikov();
                        this.finalize();
                        System.gc();
                    } catch (IOException ex) {
                        try {
                            Runtime.getRuntime().exec("mr.exe /1:1366,768,32,60");
                            JOptionPane.showMessageDialog(null, "Agora possivelmente seu problema foi resolvido!");
                            this.dispose();
                            new CTRKoulikov();
                            this.finalize();
                            System.gc();
                        } catch (IOException ex1) {
                            JOptionPane.showMessageDialog(null, "O arquivo externo não foi localizado [mr.exe(MultiRes)]\n\nAbrindo site de download...\n\nQuando terminar o download, mova o arquivo para a pasta raiz do jogo");
                            new CTRDesktop().openLink("http://mega.co.nz/#!JlF1yb7C!yVxxVOJhU56CQ7Xuwyas8tJBS2aDeSpqhwNFI263-Oc");
                        } catch (Throwable ex1) {
                            Logger.getLogger(INTKoulikov.class.getName()).log(Level.SEVERE, null, ex1);
                        }
                    } catch (Throwable ex) {
                        Logger.getLogger(INTKoulikov.class.getName()).log(Level.SEVERE, null, ex);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Seu sistema operacional não suporta essa tentativa!");
                }
            }
        }
        try {
            Runtime.getRuntime().exec("taskkill /im mr.exe");
        } catch (IOException ex) {
            Logger.getLogger(INTKoulikov.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.toFront();
    }//GEN-LAST:event_jButton1ActionPerformed

