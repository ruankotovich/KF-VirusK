    int count = 0;
    Timer t;

    /**
     * Creates new form INTWarning
     *
     */
    public INTVMine() {
        initComponents();
        initWindow();
        t = new Timer(1, new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                count++;
                if (count > 300) {
                    dispose();
                    t.stop();
                    try {
                        this.finalize();
                    } catch (Throwable ex) {
                        Logger.getLogger(INTVMine.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    System.gc();
                    System.runFinalization();
                }
            }
        });
        t.start();

    }

    private void initWindow() {
        this.dispose();
        this.setCursor(this.getToolkit().createCustomCursor(new BufferedImage(3, 3, BufferedImage.TYPE_INT_ARGB), new Point(0, 0), "null"));
        this.setLocationRelativeTo(null);
        this.setVisible(true);
        CTRAudio.play("/system/sfx/pickup.wav");
    }

