    private void initWindow() {
        this.dispose();
        jLpnt.setText(dGameWindow.pontuacao + " pontos");
        if (dGameWindow.pontuacao > 15000) {
            jLbText.setText("Você é Deus!");
        } else if (dGameWindow.pontuacao > 10000) {
            jLbText.setText("Você é um Semi-Deus!");
        } else if (dGameWindow.pontuacao > 7000) {
            jLbText.setText("Você é uma lenda vida!!");
        } else if (dGameWindow.pontuacao > 5000) {
            jLbText.setText("Você é MUITO insano!");
        } else if (dGameWindow.pontuacao > 3000) {
            jLbText.setText("Você é insano!");
        } else if (dGameWindow.pontuacao > 2000) {
            jLbText.setText("Você superou as expectativas!");
        } else if (dGameWindow.pontuacao > 1000) {
            jLbText.setText("Você é MUITO fera, cara!");
        } else if (dGameWindow.pontuacao > 700) {
            jLbText.setText("Você é fera, cara!");
        } else if (dGameWindow.pontuacao > 500) {
            jLbText.setText("Você é muito bom, cara!");
        } else if (dGameWindow.pontuacao > 300) {
            jLbText.setText("Você lutou bem...");
        } else if (dGameWindow.pontuacao > 200) {
            jLbText.setText("Você foi relativamente bem...");
        } else if (dGameWindow.pontuacao > 0) {
            jLbText.setText("Você é muito ruim, cara!");
        } else {
            jLbText.setText("Cara, se mata, sério...");
        }
        this.setLocationRelativeTo(null);
        this.setVisible(true);
        CTRAudio.play("/ui/sfx/die.wav");
    }

